// Присвоить переменной а значение в промежутке [0..15]. 
// С помощью оператора switch организовать вывод чисел от a до 15.

var a = 11;

switch (a) {
    case a = 0:
        document.write(0 + ",");
    case a = 1:
        document.write(1 + ",");
    case a = 2:
        document.write(2 + ",");
    case a = 3:
        document.write(3 + ",");
    case a = 4:
        document.write(4 + ",");
    case a = 5:
        document.write(5 + ",");
    case a = 6:
        document.write(6 + ",");
    case a = 7:
        document.write(7 + ",");
    case a = 8:
        document.write(8 + ",");
    case a = 9:
        document.write(9 + ",");
    case a = 10:
        document.write(10 + ".");
        break;
}